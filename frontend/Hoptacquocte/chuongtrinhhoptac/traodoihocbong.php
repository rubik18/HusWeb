							<p><strong>I. Chương trình trao đổi sinh viên</strong></p>

							<p>Chương trình trao đổi sinh viên là hoạt động tiếp nhận sinh viên nước ngoài hoặc gửi sinh viên của Trường Đại học Khoa học Tự nhiên, Đại học Quốc gia Hà Nội (ĐHKHTN - ĐHQGHN) nhằm giao lưu, tham quan, hoặc học tập ngắn hạn (từ vài ngày đến một học kỳ) tại các đến cơ sở của trường đại học đối tác.</p>

							<p>1. Trao đổi theo chương trình thiết kế: Là hình thức trao đổi trong đó sinh viên chỉ tham gia các chương trình tham quan, giao lưu, học tập ngắn hạn từ 1 tuần đến 3 tháng.</p>

							<p>2. Trao đổi tín chỉ (credits transfer): Là hình thức trao đổi trong đó sinh viên tham dự một số môn học phù hợp với thời hạn từ 1 tuần đến 1 năm học. Kết thúc chương trình trao đổi, tùy theo kết quả học tập sinh viên được cấp chứng chỉ hoặc/và chứng nhận cho số tín chỉ đã hoàn thành. Với hình thức trao đổi này, ĐHQGHN thừa nhận tín chỉ do các trường đối tác có uy tín cấp dùng để thay thế các môn học tương đương trong chương trình đào tạo của ĐHQGHN.</p>

							<p><a href="http://vnu.edu.vn/upload/2012/11/13843/3763%20Ban%20hanh%20Huong%20dan%20thuc%20hien%20cong%20tac%20trao%20doi%20sinh%20vien%20voi%20cac%20truong%20dai%20hoc%20nuoc%20ngoai%20tai%20Dai%20hoc%20Quoc%20gia%20Ha%20Noi.pdf">(Điều 4, 5 Quyết định số 3763/QĐ-ĐHQGHN)</a></p>

							<p><strong>II. Điều kiện tham gia chương trình</strong></p>

							<p>Điều kiện để sinh viên tham gia một chương trình trao đổi phụ thuộc vào chương trình và yêu cầu của đại học đối tác, song Trường ĐHKHTN - ĐHQGHN đưa ra một số yêu cầu chung như sau:</p>

							<p>&nbsp; &nbsp; &nbsp;- Là sinh viên năm thứ 2 đến kỳ I năm 4 hệ chính quy hoặc học viên cao học đã hoàn thành ít nhất 01 học kỳ trở lên của ĐHQGHN;</p>

							<p>&nbsp; &nbsp; &nbsp;- Có điểm trung bình chung (TBC) các học kỳ tối thiểu 2.8/4.0;</p>

							<p>&nbsp; &nbsp; &nbsp;- Có trình độ tiếng Anh tối thiểu là IELTS 5.5 hoặc tiếng Nhật tối thiểu N2 (trình độ ngoại ngữ B1 đối với một số ngoại ngữ khác);</p>

							<p>&nbsp; &nbsp; &nbsp;- Đáp ứng yêu cầu của đại học đối tác.</p>

							<p><em>Sinh viên/học viên cần tìm hiểu kỹ về yêu cầu của từng chương trình, các điều kiện trên không áp dụng với tất cả các chương trình trao đổi.</em></p>

							<p><strong>III. Chi phí tham dự</strong></p>

							<p>Khi tham gia Trao đổi, các bạn sinh viên sẽ được hỗ trợ hoàn toàn học phí; các chi phí khác như phí sinh hoạt, vé máy bay, bảo hiểm,… tùy chương trình mà sinh viên sẽ tự chi trả hoặc được hỗ trợ toàn phần/một phần.</p>

							<p><strong>IV. Hồ sơ</strong></p>

							<p>Hồ sơ đăng ký tham gia chương trình bao gồm:</p>

							<p>&nbsp; &nbsp; &nbsp;- CV;</p>

							<p>&nbsp; &nbsp; &nbsp;-&nbsp;Bảng điểm;</p>

							<p>&nbsp; &nbsp; &nbsp;-&nbsp;Bản sao hộ chiếu;</p>

							<p>&nbsp; &nbsp; &nbsp;- Thành tích hoạt động ngoại khóa (nếu có);</p>

							<p>&nbsp; &nbsp; &nbsp;- Giấy khám sức khỏe (theo mẫu có sẵn của chương trình hoặc của bệnh viện);</p>

							<p>&nbsp; &nbsp; &nbsp;- Hồ sơ theo yêu cầu của đại học đối tác.</p>

							<p>Đối với các chương trình trao đổi được ĐHQGHN thông báo thì sinh viên/học viên cần chuẩn bị hồ sơ đầy đủ theo yêu cầu của ĐHQGHN.</p>

