                                <p style="text-align: justify;">Với thế mạnh về đào tạo và nghiên cứu khoa học cơ bản, Trường ĐHKHTN xác định nghiên cứu khoa học là nhiệm vụ không thể thiếu đối với sinh viên trong quá trình học tập; giúp sinh viên không chỉ tiếp cận nghiên cứu khoa học mà còn biết vận dụng các phương pháp nghiên cứu khoa học để giải quyết một số vấn đề thực tiễn, góp phần nâng cao chất lượng đào tạo. Được sự quan tâm, tạo mọi điều kiện thuận lợi từ phía nhà trường, nghiên cứu khoa học đã thực sự trở thành hoạt động sâu rộng trong toàn trường, thu hút ngày càng nhiều sinh viên tham gia với quy mô và chất lượng ngày càng cao qua các năm. Mỗi năm, toàn trường có khoảng 500 sinh viên tham gia nghiên cứu khoa học, các nghiên cứu được thực hiện theo nhiều hướng trên nhiều lĩnh vực khác nhau. Trong đó, nhiều báo cáo được viết và trình bày bằng Tiếng Anh. Đã có nhiều báo cáo sáng tạo được Bộ Giáo dục và Đào tạo khen thưởng, một số kết quả nghiên cứu được đăng trên các tạp chí trong nước và quốc tế.</p>

