                                <p style="text-align: justify;"><strong>I. Học bổng:</strong></p>

                                <p style="text-align: justify;"><strong>1. Học bổng từ nguồn ngân sách nhà nước</strong></p>
                                <p style="text-align: justify;">Với hình thức đào tạo theo tín chỉ, nếu sinh viên đăng ký học các môn học tối thiểu 14 tín chỉ/học đối với chương trình đào tạo chuẩn; tối thiểu 18 tín chỉ/học kì đối với các chương trình đào tạo tài năng,.. có điểm rèn luyện loại khá trở lên và không có môn học nào dưới B điểm sẽ được xét cấp&nbsp;<a href="http://www.hus.vnu.edu.vn/data/sites/default/files/huongdan/Quy%20dinh%20ve%20hoc%20bong%20khuyen%20khich.pdf#overlay-context=vi/main/sinhvien/quyche"><u>học bổng Khuyến khích học tập</u></a> theo quy định của Bộ Giáo dục &amp; Đào tạo. Mức học bổng KKHT cao nhất dành cho Hệ cử nhân khoa học tài năng lên đến 1,3 triệu đồng/1 tháng. Mỗi năm số tiền học bổng khuyến khích cấp cho học sinh, sinh viên là hơn 2 tỷ đồng.&nbsp;</p>

                                <p style="text-align: justify;"><strong>2. Học bổng ngoài ngân sách nhà nước</strong></p>

                                <p style="text-align: justify;">Ngoài học bổng khuyến khích học tập theo quy định trường, học sinh sinh viên còn được nhận rất nhiều loại học bổng tài trợ của các tổ chức, cá nhân trong và ngoài nước với tổng số tiền học bổng tài trợ khoảng 3 tỷ đồng/1 năm.</p>

