                                <p style="text-align: justify;"><span style="font-size:14px;"><span style="font-family:Tahoma,Geneva,sans-serif;">Học sinh sinh viên Trường Đại học Khoa học Tự nhiên ngoài việc học tập chuyên môn, nghiên cứu khoa học còn được tham gia các hoạt động phong trào ngoại khóa do Đoàn Thanh niên - Hội sinh viên nhà trường tổ chức như văn nghệ, thể thao, các hoạt động tình nguyện vì cộng đồng... Các hoạt động phong trào được triển khai thường xuyên suốt năm học như:</span></span></p>

                                <ul>
                                    <li style="text-align: justify;"><span style="font-size:14px;"><span style="font-family:Tahoma,Geneva,sans-serif;">Tháng 09: Các hoạt động tình nguyện hỗ trợ tân sinh viên nhập học.</span></span></li>
                                    <li style="text-align: justify;"><span style="font-size:14px;"><span style="font-family:Tahoma,Geneva,sans-serif;">Tháng 10: Chương trình Tổng kết hoạt động tình nguyện và chào tân sinh viên.</span></span></li>
                                    <li style="text-align: justify;"><span style="font-size:14px;"><span style="font-family:Tahoma,Geneva,sans-serif;">Tháng 11: Các hoạt động chào mừng ngày nhà giáo Việt Nam.</span></span></li>
                                    <li style="text-align: justify;"><span style="font-size:14px;"><span style="font-family:Tahoma,Geneva,sans-serif;">Tháng 02: Cuộc thi Aerobic học sinh sinh viên “Storm from HUS”, cuộc thi báo tường mừng Ngày thành lập Đảng.</span></span></li>
                                    <li style="text-align: justify;"><span style="font-size:14px;"><span style="font-family:Tahoma,Geneva,sans-serif;">Tháng 03: Chương trình những ngày hội tháng Ba (hoạt động cắm trại, liên hoan văn nghệ, về nguồn,…), Festival tài năng học sinh, sinh viên, Festival thể thao (với 04 nội dung: cờ vua, cầu lông, bóng chuyền, bóng đá).</span></span></li>
                                    <li style="text-align: justify;"><span style="font-size:14px;"><span style="font-family:Tahoma,Geneva,sans-serif;">Tháng 04: Festival các câu lạc bộ, cuộc thi Miss &amp; Mr svtunhien (được tổ chức 02 năm một lần).</span></span></li>
                                    <li style="text-align: justify;"><span style="font-size:14px;"><span style="font-family:Tahoma,Geneva,sans-serif;">Tháng 05: Hội nghị các nhà khoa học trẻ trong lĩnh vực Khoa học tự nhiên và Công nghệ.</span></span></li>
                                    <li style="text-align: justify;"><span style="font-size:14px;"><span style="font-family:Tahoma,Geneva,sans-serif;">Tháng 06, 07, 08: Hoạt động tình nguyện vì cộng đồng.</span></span></li>
                                </ul>

                                <p style="text-align: justify;"><span style="font-size:14px;"><span style="font-family:Tahoma,Geneva,sans-serif;">&nbsp;Hiện nay, Trường ĐHKHTN là một trong những trường có hoạt động tình nguyện đa dạng nhất, với hàng loạt mảng hoạt động chính như: “Tiếp sức mùa thi tuyển sinh THPT chuyên”, “Tiếp sức mùa thi tuyển sinh đại học, cao đẳng”, “Xung kích tại các địa phương”, “Làng trẻ”, “Đội sinh viên tình nguyện hoạt động vì môi trường”, v.v… Địa bàn hoạt động của các đội trải rộng từ Hà Nội tới các tỉnh như Hải Dương, Nam Định, Hòa Bình, Thái Nguyên, Sơn La, Cao Bằng, Bắc Giang,…</span></span></p>

                                <p style="text-align: justify;"><span style="font-size:14px;"><span style="font-family:Tahoma,Geneva,sans-serif;">Hoạt động tình nguyện của Trường đã để lại dấu ấn trong lòng hàng nghìn hộ dân tại các địa phương nơi đội xung kích hoạt động, hàng vạn thí sinh thi vào các trường Đại học và Cao đẳng tại Hà Nội, Hà Tây và trong lòng nhiều em nhỏ của các làng trẻ. Các hoạt động này cũng để lại tình cảm sâu sắc trong lòng hàng nghìn sinh viên trực tiếp hay gián tiếp tham gia các hoạt động tình nguyện của trường. Hàng năm, trong những lễ tổng kết chiến dịch tình nguyện, trường đều đạt nhiều bằng khen của Trung Ương Đoàn và Thành Đoàn Hà Nội.</span></span></p>

