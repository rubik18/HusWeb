                            <li>Lớp chuyên Tin học: nộp hồ sơ và học phí tại Giảng đường 205; Giảng đường 206</li>
                        </ul>

                        <p><em><strong>2. Chiều ngày 01/8/2020:</strong></em> Từ 13h30’-17h00’</p>

                        <p>*Từ 13h30’- 15h00’:  Học sinh có tên từ A đến L( ví dụ: An, Anh,...Đức...Linh)</p>

                        <p>*Từ 15h00’- 16h30’:  Học sinh có tên từ M đến Y( ví dụ: Mai, Nam,...... Yến)</p>

                        <ul>
                            <li>Lớp Chất lượng cao: nộp hồ sơ và học phí tại Giảng đường 201; Giảng đường 202</li>
                            <li>Lớp chuyên Vật lý học: nộp hồ sơ và học phí tại Giảng đường 203; Giảng đường 204</li>
                            <li>Lớp chuyên Hóa học: nộp hồ sơ và học phí tại Giảng đường 205; Giảng đường 206</li>
