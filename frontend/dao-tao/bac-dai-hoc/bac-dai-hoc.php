                                                <p style="text-align: justify;">
                                                    <span style="font-size:14px;">
                                                        <span style="font-family:Tahoma,Geneva,sans-serif;">
                                                            <strong>1. Ngành Địa chất</strong>
                                                        </span>
                                                    </span>
                                                </p>

                                                <p style="text-align: justify;">
                                                    <span style="font-size:14px;">
                                                        <span style="font-family:Tahoma,Geneva,sans-serif;">
                                                            <em><strong>Mục tiêu đào tạo:</strong></em>&nbsp;Trang bị cho sinh viên những kiến thức đại cương về khoa học tự nhiên, khoa học xã hội, kiến thức cơ bản ngành Địa chất, kiến thức chuyên sâu và cập nhật những tiến bộ khoa học Địa chất thế giới và Địa chất Việt Nam. Rèn luyện cho sinh viên phương pháp nghiên cứu khoa học, làm việc độc lập, kỹ năng tổ chức hợp tác thực hiện nhiệm vụ và khả năng trình bày báo cáo các kết quả nghiên cứu khoa học và thực hiện nhiệm vụ.
                                                        </span>
                                                    </span>
                                                </p>

                                                <p style="text-align: justify;">
                                                    <span style="font-size:14px;">
                                                        <span style="font-family:Tahoma,Geneva,sans-serif;">
                                                            <em><strong>Triển vọng nghề nghiệp:</strong></em>&nbsp;Sinh viên sau khi tốt nghiệp có đủ năng lực làm việc ở trong các viện, cơ quan nghiên cứu Khoa học Trái đất, địa chất dầu khí, địa kỹ thuật, cơ quan quản lý nhà nước, các Sở Tài nguyên và Môi trường, Khoa học Công nghệ, các doanh nghiệp thi công xây dựng cơ sở hạ tầng, các công ty liên doanh ở trong và ngoài&nbsp; nước như thăm dò khoáng sản,… Tham gia thực hiện các phương án đo vẽ bản đồ địa chất khoáng sản, các dự án và đề tài quản lý, đánh giá tác động môi trường, phát triển bền vững lãnh thổ và lãnh hải phục vụ xây dựng cơ sở hạ tầng giao thông, đô thị trong quá trình công nghiệp hóa hiện đại hóa Đất nước hoặc được đào tạo tiếp thạc sĩ và tiến sĩ trong và ngoài nước.
                                                        </span>
                                                    </span>
