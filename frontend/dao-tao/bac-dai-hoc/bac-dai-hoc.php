                                                    <span style="font-size:14px;">
                                                        <span style="font-family:Tahoma,Geneva,sans-serif;">Các hướng đào tạo đang tập trung: Nghiên cứu phân tích các cấu trúc địa chất và các quá trình địa chất phục vụ xây dựng các cầu cảng, các công trình đập thuỷ điện, đường giao thông, quy hoạch lãnh thổ, đánh giá triển vọng dầu khí; công nghệ khai thác tài nguyên, gia công chế tác các loại ngọc và đá quý. Ngoài ra còn trang bị những kiến thức hiện đại về hệ thống thông tin địa lý và viễn thám - một công cụ mạnh hiện nay, phục vụ nhiều ngành nhiều lĩnh vực.
                                                        </span>
                                                    </span>
                                                </p>

                                                <p style="text-align: justify;">
                                                    <span style="font-size:14px;">
                                                        <span style="font-family:Tahoma,Geneva,sans-serif;">
                                                            <strong>2. Ngành Địa Kỹ thuật – Địa Môi trường</strong>
                                                        </span>
                                                    </span>
                                                </p>

                                                <p style="text-align: justify;">
                                                    <span style="font-size:14px;">
                                                        <span style="font-family:Tahoma,Geneva,sans-serif;">
                                                            <em><strong>Mục tiêu đào tạo:</strong></em>&nbsp;Trang bị cho sinh viên những kiến thức đại cương về khoa học tự nhiên, khoa học xã hội, kiến thức cơ bản ngành Địa kỹ thuật - Địa môi trường cơ bản và hiện đại, cập nhật những tiến bộ khoa học về Địa kỹ thuật, Địa môi trường và Địa vật lý ứng dụng ở Việt Nam và trên Thế giới. Rèn luyện cho sinh viên phương pháp nghiên cứu khoa học, phương pháp làm việc độc lập, khả năng tổ chức hợp tác trong thực hiện nhiệm vụ và khả năng trình bày báo cáo các kết quả nghiên cứu khoa học.
                                                        </span>
                                                    </span>
                                                </p>

                                                <p style="text-align: justify;">
                                                    <span style="font-size:14px;">
                                                        <span style="font-family:Tahoma,Geneva,sans-serif;">
                                                            <em><strong>Triển vọng nghề nghiệp:&nbsp;</strong></em>Sinh viên sau khi tốt nghiệp có đủ năng lực làm việc về các lĩnh vực thuộc khoa học Trái đất, các viện và các trung tâm, các tổng công ty về xây dựng, giao thông, thủy lợi, các sở giao thông công chính, khoa học-công nghệ, tài nguyên-môi trường của các tỉnh và thành phố, tham gia thực hiện các dự án đo vẽ bản đồ địa chất công trình và địa chất thủy văn, các dự án và đề tài quản lý, đánh giá tác động môi trường, phát triển bền vững lãnh thổ và lãnh hải phục vụ xây dựng cơ sở hạ tầng trong quá trình công nghiệp hóa hiện đại hóa Đất nước. Giảng dạy tại các trường đại học và cao đẳng hoặc có thể được đào tạo tiếp thạc sĩ, tiến sĩ ở trong và ngoài nước.
                                                        </span>
                                                    </span>
                                                </p>

                                                <p style="text-align: justify;">
                                                    <span style="font-size:14px;">
                                                        <span style="font-family:Tahoma,Geneva,sans-serif;">Các hướng đang tập trung đào tạo, nghiên cứu: cấu trúc nền móng công trình, giải pháp xử lý các nền đất yếu, các sự cố trong xây dựng công trình (cầu, cảng, đập thuỷ điện, móng nhà cao tầng); cơ chế, quy luật phát sinh ô nhiễm môi trường nước, đất và khí; các loại tai biến như động đất, núi lửa, lũ lụt, trượt lở, nứt đất, sóng thần; các giải pháp phòng tránh và giảm thiểu tai biến; các phương pháp địa vật lý (phương pháp địa chấn, phương pháp điện, phương pháp từ, phương pháp trọng lực, phương pháp phóng xạ,...) để nghiên cứu vỏ Trái đất, tìm kiếm khoáng sản, đặc biệt là dầu khí, tài nguyên nước, nghiên cứu cấu trúc và bảo vệ nguyên trạng các công trình cổ.
                                                        </span>
                                                    </span>
                                                </p>

                                                <p style="text-align: justify;">
                                                    <span style="font-size:14px;">
                                                        <span style="font-family:Tahoma,Geneva,sans-serif;">
                                                            <strong>3. Ngành Quản lý Tài nguyên thiên nhiên</strong>
                                                        </span>
                                                    </span>
                                                </p>

                                                <p style="text-align: justify;">
                                                    <span style="font-size:14px;">
                                                        <span style="font-family:Tahoma,Geneva,sans-serif;">Nước ta là một quốc gia giàu có về tài nguyên (tài nguyên đất, tài nguyên rừng, tài nguyên biển, tài nguyên địa chất,...) nhưng đội ngũ cán bộ về quản lý tài nguyên thiên nhiên còn rất hạn chế và chưa được đào tạo theo một hệ thống. Trước đòi hỏi của Đất nước và xu thế hội nhập khu vực và Quốc tế, Khoa Địa chất Trường Đại học Khoa học Tự nhiên được giao nhiệm vụ đào tạo cử nhân Quản lý tài nguyên thiên nhiên từ năm 2008.
                                                        </span>
                                                    </span>
                                                </p>

                                                <p style="text-align: justify;">
                                                    <span style="font-size:14px;">
                                                        <span style="font-family:Tahoma,Geneva,sans-serif;">
