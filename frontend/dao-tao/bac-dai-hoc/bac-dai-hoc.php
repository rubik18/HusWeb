                                                <p style="text-align: justify;">
                                                    <span style="font-size:14px;">
                                                        <span style="font-family:Tahoma,Geneva,sans-serif;">
                                                            <strong>1. Ngành Khoa học Môi trường</strong>
                                                        </span>
                                                    </span>
                                                </p>

                                                <p style="text-align: justify;">
                                                    <span style="font-size:14px;">
                                                        <span style="font-family:Tahoma,Geneva,sans-serif;">
                                                            <em><strong>Mục tiêu đào tạo:</strong></em>&nbsp;Trang bị cho sinh viên những kiến thức đại cương về khoa học tự nhiên, khoa học xã hội, ngoại ngữ, ngoại ngữ. Đặc biệt là các kiến thức chuyên sâu đa dạng về khoa học môi trường, các kỹ năng quản lý, nghiên cứu và thực hiện các chương trình dự án về môi trường. Những kỹ năng phân tích, đánh giá môi trường các vùng lãnh thổ, sử dụng các công cụ tin học, hệ thông tin địa lý và kỹ thuật viễn thám trong nghiên cứu môi trường, đánh giá tác động môi trường các hoạt động kinh tế xã hội, quy hoạch môi trường, đề xuất các công cụ luật pháp, chính sách và kinh tế cho công tác bảo vệ môi trường.
                                                        </span>
                                                    </span>
                                                </p>

                                                <p style="text-align: justify;">
                                                    <span style="font-size:14px;">
                                                        <span style="font-family:Tahoma,Geneva,sans-serif;">
                                                            <em><strong>Triển vọng nghề nghiệp:</strong></em>&nbsp;Cử nhân Khoa học môi trường có đủ năng lực giảng dạy, nghiên cứu, quản lý tại các trường đại học và cao đẳng, các viện và trung tâm nghiên cứu khoa học, các cơ quan quản lý như Bộ tài nguyên-Môi trường, các sở Tài nguyên-Môi trường, các phòng Tài nguyên-Môi trường ở các huyện, các nhà máy xí nghiệp, các cơ sở sản xuất kinh doanh, các dự án có liên quan tới quản lý, quy hoạch môi trường, các dự án khai thác và sử dụng tài nguyên thiên nhiên có liên quan đến lĩnh vực môi trường và khai thác tài nguyên hoặc có thể được đào tạo tiếp thạc sĩ, tiến sĩ ở trong và ngoài nước.
                                                        </span>
                                                    </span>
                                                </p>

                                                <p style="text-align: justify;">
                                                    <span style="font-size:14px;">
                                                        <span style="font-family:Tahoma,Geneva,sans-serif;">Một số hướng nghiên cứu chính: Sinh thái nhân văn, an toàn môi trường và phát triển bền vững; ảnh hưởng của các điều kiện kinh tế xã hội, các tác động của sự thay đổi các điều kiện tự nhiên (đất, nước, không khí,…) đến hệ sinh thái; sinh thái môi trường ứng dụng, chỉ thị sinh học môi trường; cơ chế và động học của các quá trình công nghệ trong xử lý ô nhiễm môi trường; độc chất môi trường, sự tích lũy, các quá trình lan nhiễm, tác động đến môi trường và hệ sinh thái, các biện pháp công nghệ xử lý ô nhiễm môi trường; Quản lý môi trường, Qui hoạch môi trường, Phân tích đánh giá môi trường và Mô hình hóa môi trường; Ứng dụng viễn thám và GIS trong nghiên cứu môi trường;&nbsp; Quản lý, khai thác, sử dụng hợp lý tài nguyên thiên nhiên; Qui hoạch sử dụng hợp lý tài nguyên đất, Kiểm soát và xử lý ô nhiễm môi trường đất.
                                                        </span>
                                                    </span>
                                                </p>

                                                <p style="text-align: justify;">
                                                    <span style="font-size:14px;">
                                                        <span style="font-family:Tahoma,Geneva,sans-serif;">
                                                            <strong>2. Ngành Khoa học đất</strong>
                                                        </span>
                                                    </span>
                                                </p>

